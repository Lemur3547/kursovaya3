from units import operations


def test_sort_operations():
    assert operations.sort_operations(
        [{"date": "2019-08-26T10:50:58.294041"},
         {"date": "2018-06-30T02:08:58.425572"},
         {"date": "2019-07-03T18:35:29.512364"}]
    ) == [
               {"date": "2019-08-26T10:50:58.294041"},
               {"date": "2019-07-03T18:35:29.512364"},
               {"date": "2018-06-30T02:08:58.425572"}]

    assert operations.sort_operations(
        [{"date": "2019-08-26T10:50:58.294041"},
         {"date": "2019-07-03T18:35:29.512364"},
         {"id": 41428829}]
    ) == [{"id": 41428829},
          {"date": "2019-08-26T10:50:58.294041"},
          {"date": "2019-07-03T18:35:29.512364"}]

    assert operations.sort_operations(
        [{"date": "2019-08-26T10:50:58.294041"},
         {"date": "2019-07-03T18:35:29.512364"},
         {}]
    ) == [{},
          {"date": "2019-08-26T10:50:58.294041"},
          {"date": "2019-07-03T18:35:29.512364"}]
